// Helper to get recipes by type
export function getRecipesByType(type: Recipe['type']): Recipe[] {
  return recipes.filter(r => r.type === type);
}

// Helper to get recipes by category (veg/non-veg)
export function getRecipesByCategory(category: Recipe['category']): Recipe[] {
  return recipes.filter(r => r.category === category);
}

// Helper to get recipes for a meal type
export function getRecipesForMeal(mealType: 'breakfast' | 'lunch' | 'dinner' | 'snack'): Recipe[] {
  return recipes.filter(r => r.mealTypes.includes(mealType));
}

// Helper to get recipes by region
export function getRecipesByRegion(region: Region): Recipe[] {
  return recipes.filter(r => r.region === region || r.region === 'pan_india');
}

