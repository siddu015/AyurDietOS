export interface RecipeIngredient {
  foodId: string;
  quantity: number;
  unit: 'g' | 'ml' | 'tsp' | 'tbsp' | 'cup' | 'piece';
  optional?: boolean;
}

export interface Recipe {
  id: string;
  name: string;
  nameHindi: string;
  type: 'breakfast' | 'main_course' | 'side_dish' | 'snack' | 'dessert' | 'beverage' | 'accompaniment';
  category: 'vegetarian' | 'vegan' | 'non_vegetarian' | 'eggetarian';
  ingredients: RecipeIngredient[];
  preparationMethod: PreparationMethod;
  cookingTime: number; // in minutes
  servings: number;
  region: Region;
  mealTypes: ('breakfast' | 'lunch' | 'dinner' | 'snack')[];
  season?: string[];
  healthBenefits?: string[];
  contraindications?: string[];
  instructions?: string[];
}

export type PreparationMethod = 
  | 'boiled'
  | 'steamed' 
  | 'fried'
  | 'roasted'
  | 'raw'
  | 'fermented'
  | 'tempered'
  | 'pressure_cooked'
  | 'sauteed'
  | 'baked'
  | 'grilled';

export type Region = 
  | 'north_india'
  | 'south_india' 
  | 'east_india'
  | 'west_india'
  | 'pan_india';

